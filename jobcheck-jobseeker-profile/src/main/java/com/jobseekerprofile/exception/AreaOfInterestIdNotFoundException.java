package com.jobseekerprofile.exception;

public class AreaOfInterestIdNotFoundException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AreaOfInterestIdNotFoundException() {
		super();
	}

	public AreaOfInterestIdNotFoundException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AreaOfInterestIdNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public AreaOfInterestIdNotFoundException(String message) {
		super(message);
	}

	public AreaOfInterestIdNotFoundException(Throwable cause) {
		super(cause);
	}
	
	
}
