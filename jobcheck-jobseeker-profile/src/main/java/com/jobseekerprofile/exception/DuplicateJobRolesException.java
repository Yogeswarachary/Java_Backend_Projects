package com.jobseekerprofile.exception;

public class DuplicateJobRolesException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DuplicateJobRolesException() {
		super();
	}

	public DuplicateJobRolesException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DuplicateJobRolesException(String message, Throwable cause) {
		super(message, cause);
	}

	public DuplicateJobRolesException(String message) {
		super(message);
	}

	public DuplicateJobRolesException(Throwable cause) {
		super(cause);
	}
	
	

}
