package com.jobseekerprofile.exception;

public class DuplicateAreaOfIntrestException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DuplicateAreaOfIntrestException() {
		super();
	}

	public DuplicateAreaOfIntrestException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DuplicateAreaOfIntrestException(String message, Throwable cause) {
		super(message, cause);
	}

	public DuplicateAreaOfIntrestException(String message) {
		super(message);
	}

	public DuplicateAreaOfIntrestException(Throwable cause) {
		super(cause);
	}
	
	

}
