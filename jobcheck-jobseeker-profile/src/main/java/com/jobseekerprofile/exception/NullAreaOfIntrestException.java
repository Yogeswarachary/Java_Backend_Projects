package com.jobseekerprofile.exception;

public class NullAreaOfIntrestException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NullAreaOfIntrestException() {
		super();
	}

	public NullAreaOfIntrestException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NullAreaOfIntrestException(String message, Throwable cause) {
		super(message, cause);
	}

	public NullAreaOfIntrestException(String message) {
		super(message);
	}

	public NullAreaOfIntrestException(Throwable cause) {
		super(cause);
	}
	
}
