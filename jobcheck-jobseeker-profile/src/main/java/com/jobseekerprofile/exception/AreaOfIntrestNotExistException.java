package com.jobseekerprofile.exception;

public class AreaOfIntrestNotExistException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AreaOfIntrestNotExistException() {
		super();
	}

	public AreaOfIntrestNotExistException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AreaOfIntrestNotExistException(String message, Throwable cause) {
		super(message, cause);
	}

	public AreaOfIntrestNotExistException(String message) {
		super(message);
	}

	public AreaOfIntrestNotExistException(Throwable cause) {
		super(cause);
	}
	
	
}
