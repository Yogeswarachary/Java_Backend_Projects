package com.jobseekerprofile.exception;

public class BasicEducationNotFoundException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public BasicEducationNotFoundException() {
		super();
	}

	public BasicEducationNotFoundException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BasicEducationNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public BasicEducationNotFoundException(String message) {
		super(message);
	}

	public BasicEducationNotFoundException(Throwable cause) {
		super(cause);
	}
	
	

}
